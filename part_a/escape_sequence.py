# Escape sequences in Python

# Using backslash (\) to include special characters in strings

weather = "It\'s \"kind of\" sunny"
weather1 = 'It\\s "kind of" sunny'

# Using tab (\t) and newline (\n) escape sequences
weather2 = "\t It\'s \"kind of\" sunny"
weather3 = "\t It's \"kind of\" sunny \n Hope you have a good day!"

# Printing the strings to see the effect of escape sequences
print(weather) 
print(weather1)
print(weather2)
print(weather3)
